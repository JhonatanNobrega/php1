<?php

    /*
    |   Variavel são informações que guardamos os mais diversos tipo de valores para depois usarmos.
    |   Para criar uma variavel no PHP fazemos o seguinte $nomeVariavel = 'Valor da variavel'.
    |   Temos libertade de escolher o nome da variavel. 
    |   Isso é util para escrever nossos código de forma legivel para outros programadores.

    |   O nome da variavel NUNCA pode começar com um numero e NUNCA pode ter um TRAÇO.
    |   |_ NUNCA pode ter espaço ou caracteres especiais.
    |   O nome da variavel pode começar com letra minusculas e maiusculas.
    |   Para usar mais que um nome podemos fazer os exemplos baixo.
    |   Exemplo: $nome_completo ou $nomeCompleto
    |   Caso usamos undeline é interesante todo sistema usar esse padrão ou então apenas o segundo exemplo.
    */

    // Variavel com valor 'inteiro'
